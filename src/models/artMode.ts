/**
 * An enumeration of the different art modes the widget can operate in.
 */
export enum ArtMode {
    /**
     * Show no artwork.
     */
    None,

    /**
     * Show the track's album artwork.
     */
    AlbumArt,

    /**
     * Show a custom image.
     */
    Custom,

    /**
     * Show a preset image.
     */
    Premade,
}
